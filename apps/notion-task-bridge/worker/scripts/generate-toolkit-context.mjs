import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, "..", "..", "..", "..");
const outDir = join(__dirname, "..", "src", "generated");
const outFile = join(outDir, "toolkit-context.ts");

const files = [
	["projectManager", join(repoRoot, "global", "agents", "project-manager.md")],
	["pipeline", join(repoRoot, "global", "rules", "pipeline.md")],
	["notionRules", join(repoRoot, "global", "rules", "notion.md")],
	["gitRules", join(repoRoot, "global", "rules", "git.md")],
	["taskCommand", join(repoRoot, "global", "commands", "task.md")],
];

function esc(s) {
	return s.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$/g, "\\$");
}

mkdirSync(outDir, { recursive: true });

const parts = [];
for (const [key, path] of files) {
	const text = readFileSync(path, "utf8");
	parts.push(`export const ${key} = \`${esc(text)}\`;`);
}

const header = `/* eslint-disable */\n// Generated by scripts/generate-toolkit-context.mjs — do not edit by hand.\n`;

writeFileSync(outFile, `${header}${parts.join("\n\n")}\n`, "utf8");
console.log("Wrote", outFile);
